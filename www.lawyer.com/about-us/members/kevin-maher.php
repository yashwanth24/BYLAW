	<h1>Kevin Maher: Director of Acquisitions & International</h1>
	<b class="c8"></b><b class="c7"></b><b class="c6"></b><b class="c5"></b>
</div>
<table width="750" cellpadding="0" cellspacing="0"><tr>
<td valign="top" width="420">
<img src="../img/kmbio.png" align="left">
<p>Kevin Maher is the Director of Acquisitions for Lawyer.com. His focus is on Traffic Analysis, and enhancing the Online Presence of current clients. Kevin also manages the brand’s International expansion (North America, Europe, and beyond).</p>

<p>Kevin started with Lawyer.com in 2012 as a Member Representative. From the start, Kevin showcased great customer service and sales skills. He quickly gained new roles managing the company’s Pay Per Click (PPC) and Search Engine Optimization (SEO). Kevin is Google AdWords certified, and has experience with Google Analytics. He is responsible for establishing Lawyer.com’s presence on social media. He also managed full-service websites for Lawyers, known as Lawyer.com LawSites.</p>

<p>Kevin has shared his knowledge and expertise during CLE-approved engagements, in New Jersey and Missouri, speaking on topics centered in online marketing for lawyers. He has represented Lawyer.com in partnered events with BarBri in Washington D.C. and New York City. While frequenting legal conferences around the country, he focuses on further establishing the Lawyer.com brand and gaining new clients.</p>

<p>Prior to Lawyer.com, Kevin began his career working for an entertainment website in Manhattan. He had also managed golf events at the largest private country club in the country, located in New Jersey.</p>

<p>Kevin holds a Bachelor of Science degree from Westwood College. When not at work, you can catch Kevin watching worldwide soccer games, discussing the psychology of Batman, or planning the next road trip with his friends.</p>
